using System;

namespace ConsoleApp14
{
    public class math
    {
        public static int PowerOf2(int userInput)
        {
            int answer;
            answer = userInput * userInput;
            return answer;
        }

        public static int PowerOf3(int userInput)
        {
            int answer;
            answer = userInput * userInput * userInput;
            return answer;
        }

        public static int PowerOf4(int userInput)
        {
            int answer;
            answer = userInput * userInput * userInput * userInput;
            return answer;
        }

    }
}
